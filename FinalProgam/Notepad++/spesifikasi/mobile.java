package spesifikasi;

public class mobile {
    private String Prosesor;
    private String Ram;
    private String Gpu;
    private String Layar;
    private String Kamera;
    private String OS;

    public String getProsesor() {
        return Prosesor;
    }

    public void setProsesor(String Prosesor) {
        this.Prosesor = Prosesor;
    }

    public String getRam() {
        return Ram;
    }

    public void setRam(String Ram) {
        this.Ram = Ram;
    }

    public String getGpu() {
        return Gpu;
    }

    public void setGpu(String Gpu) {
        this.Gpu = Gpu;
    }

    public String getLayar() {
        return Layar;
    }

    public void setLayar(String Layar) {
        this.Layar = Layar;
    }

    public String getKamera() {
        return Kamera;
    }

    public void setKamera(String Kamera) {
        this.Kamera = Kamera;
    }

    public String getOS() {
        return OS;
    }

    public void setOS(String OS) {
        this.OS = OS;
    }   
}